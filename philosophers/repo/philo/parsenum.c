#include "philo.h"

t_bool	parsenum(char *str, t_ulong *n)
{
	*n = 0;
	while (*str >= '0' && *str <= '9')
	{
		*n = *n * 10 + (*str - '0');
		str++;
	}
	return (*str == '\0');
}
